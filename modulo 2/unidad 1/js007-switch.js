let dia = "domingo";

switch (dia) {
    case "lunes":
        console.log("Laboral");
        break;
    case "martes":
        console.log("Martes Navidad");
        break;
    case "miercoles":
        console.log("Miercoles Halloween");
        break;
    case "jueves":
        console.log("Laboral");
        break;
    case "viernes":
        console.log("Viernes santo");
        break;
    case "sabado":
        console.log("Sábado de Gloria");
        break;
    case "domingo":
        console.log("No Laboral");
        break;
    default:
        console.log("Día no válido")
}